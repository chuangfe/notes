// nvm 是用來控制 node.js 版本的軟體，可以從網路找 windows 版本

// 使用說明
// 需要注意有沒有設置 nvm 的環境變數

// nvm 指令

// 可以查看 nvm 的指令
// nvm

// 查看當前已安裝的 node.js 版本，版本前如果有星號代表當前使用的 node.js 版本
// nvm list

// 查看所以有可以安裝的 node.js 版本
// 建議安裝 LTS 的最新版，這是穩定版
// nvm list available

// 安裝指定的 node.js 版本
// nvm install v[版本]

// 刪除指定的 node.js 版本
// nvm uninstall v[版本]

// 切換到指定的 node.js 版本
// nvm use [版本]

// nodejs 在 windows 7 支援的最後版本
// 13.14.0
