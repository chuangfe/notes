/**
 * git rebase 分支名稱
 *
 * 介紹, 將兩個分支合併, 有很多更詳細的使用方式, 待補充.
 *
 * 補充, 語意應該為, 將其他分支, 合併到, 當前分支.
 *
 * 補充, 合併結果為將兩個分支合併到一條線上, 中間可能會出問題, 解決衝突後可能會需要再輸入以下指令的其中一種.
 * git rebase --continue 繼續.
 * git rebase --abort 終止.
 * git rebase --skip 跳過.
 *
 * 正常步驟.
 * 1. 在其他分支合併至 master 分支.
 * 2. 解決衝突後, 輸入 commit 紀錄.
 * 3. 輸入 git rebase --continue 指令.
 */

