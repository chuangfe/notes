// Math.pow(數字, 次方數);
// 返回數字次方數後的值
// 返回數字開根號後的值

let n = Math.pow(9, 2);

console.log(n); // 81

// -----------------------------------------------------------------------------

// 計算 8 開 3 次根號
// 補充 Math.sqrt() 的效率較高，請優先使用

n = Math.pow(8, 1 / 3);

console.log(n); // 2

// -----------------------------------------------------------------------------

// 補充

// 計算 2 的 5 次方
console.log(1 << 5); // 32
