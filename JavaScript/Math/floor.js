// Math.floor(數字);
// 返回向下捨入的數字

let num = Math.floor(4.7);

console.log(num); // 4

// -----------------------------------------------------------------------------

num = Math.floor(4.1);

console.log(num); // 4

// -----------------------------------------------------------------------------

num = Math.floor(-4.7);

console.log(num); // -5

// -----------------------------------------------------------------------------

num = Math.floor(-4.1);

console.log(num); // -5
