// Math.ceil(數字);
// 返回向上捨入的數字

let num = Math.ceil(4.7);

console.log(num); // 5

// -----------------------------------------------------------------------------

num = Math.ceil(4.1);

console.log(num); // 5

// -----------------------------------------------------------------------------

num = Math.ceil(-4.7);

console.log(num); // -4

// -----------------------------------------------------------------------------

num = Math.ceil(-4.1);

console.log(num); // -4
