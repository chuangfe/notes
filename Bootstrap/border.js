// 邊框相關的 class 類別.

/**
 * 設定邊框.
 *
 * border-{type}, type 可有可無, 沒有 type 代表四個方向的邊框都要有.
 * type:
 * top: top.
 * end: right.
 * bottom: bottom.
 * start: left.
 */

/**
 * 減少邊框.
 * border-{type}-0, , type 可有可無, 0 代表不要邊框.
 */

/**
 * 邊框顏色.
 * border-{color}.
 *
 * color:
 * primary, 藍色.
 * secondary, 灰色.
 * success, 綠色.
 * danger, 紅色.
 * warning, 黃色.
 * info, 青色.
 * light, 白色.
 * dark, 黑色.
 * white, 白色.
 */

/**
 * 邊框寬度.
 * border-{number}.
 * number: 1, 2, 3, 4, 5.
 */

/**
 * 邊框圓角.
 * rounded-{type}, type 可有可無, 沒有 type 代表全部都是圓角.
 * type:
 * top: 左上 + 右上.
 * end: 右上 + 右下.
 * bottom: 左下 + 右下.
 * start: 左上 + 左下.
 * circle: 變成圓形.
 * pill: 變成橫著的橢圓形.
 *
 * 圓角尺寸.
 * rounded-{number}.
 * number: 0, 1, 2, 3.
 */
