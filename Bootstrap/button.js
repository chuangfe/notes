// 按鈕相關 class 樣式.

/**
 * 將 button 原生預設的樣式刪除.
 * btn
 */

/**
 * 將一個 tag 包裝成 button 的樣式.
 * btn-{type}, type 可有可無, 控制按鈕大小.
 * type: lg, sm.
 *
 * 範例, <button class="btn btn-sm"></button>
 */

/**
 * 按鈕顏色.
 * btn-{type}-{color}.
 * type: outline, type 可有可無, 若沒有 color 就是背景顏色, 若有 color 就是邊框和文字的顏色且沒有背景顏色.
 *
 * color:
 * primary, 藍色.
 * secondary, 灰色.
 * success, 綠色.
 * danger, 紅色.
 * warning, 黃色.
 * info, 青色.
 * light, 白色.
 * dark, 黑色.
 * link, tag a 的樣式.
 */

/**
 * 按鈕禁用.
 * input button 加上 disabled 屬性即可.
 * a tag 需要加上 disabled class.
 */
