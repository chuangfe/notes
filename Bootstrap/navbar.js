// navbar 系列 class 功能.

/**
 * navbar, 是 bootstrap 自訂的導覽列的最外層容器, 包含一些已設計好的樣式.
 *
 * navbar-expand-{breakpoints}, 通常這個 class 是給 navbar 的, 需要配合子元素的特定樣式(單獨使用沒有效果), 可以讓子元素因為父元素的斷點, 進行不同的樣式改變, 也就是所謂的響應式設計.
 *
 * navbar-light, navbar 的顏色, 包含 navbar 與 nav-link.
 */

/**
 * navbar-collapse, 是 navbar 子元素的容器, 該 class 會對應 navbar 的斷點, 從水平排列, 修改為垂直排列, 補充該 class 不能與 container 一起使用.
 *
 * collapse, 是 navbar 子元素的容器, 該 class 會對應 navbar 的斷點, 進行隱藏內容.
 */

/**
 * navbar-nav, 是 navbar-collapse 的子元素, 可以刪除 list 的樣式, 並且讓 li 水平排列.
 * nav-item, 是 navbar-nav 的子元素.
 *
 * nav-link, 是 nav-item 的子元素.
 *
 * dropdown-toggle, 可以有一個倒三角形按鈕.
 */

