// 文字相關的 class 類別.

/**
 * 文字粗體.
 * fw-{type}.
 * type: bold, normal, light, lighter.
 */

/**
 * 文字斜體.
 * fst-{type}.
 * type: italic(斜體), normal.
 */

/**
 * 文字裝飾.
 *
 * text-decoration-underline, 文字底線.
 * text-decoration-line-through, 文字中劃線.
 * text-decoration-none, 文字取消裝飾.
 */

/**
 * 使用 class 代替原生的 tag.
 * mark -> .mark
 * small -> .small
 * u -> .text-decoration-underline
 * s -> .text-decoration-line-through
 */
